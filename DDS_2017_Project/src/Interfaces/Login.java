    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // boton aceptar

        if (!usernameTextField.getText().isEmpty() && passField.getPassword().length != 0) {
            jLabel3.setText("");
            String usuario = usernameTextField.getText();
            String pass = passField.getText();
            GestorAutenticacion control = new GestorAutenticacion();
            List lista = control.autenticar(usuario, pass);

            if (!lista.isEmpty()) {
                PrincipalAdmin v1 = new PrincipalAdmin();
                v1.setNombreApellido(lista);
                this.dispose();
                v1.setVisible(true);

            } else {
                jLabel3.setText("Datos incorrectos");
            }

        } else {
            jLabel3.setText("Complete todos los datos");
        }


    }//GEN-LAST:event_jButton1ActionPerformed

